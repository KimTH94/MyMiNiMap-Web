package RecommendMenus;

public class RecommendMenusDTO {

	private double SEQ_RecommendMenus;
	private double SEQ_User;
	private double SEQ_Post;
	private String Menu;
	private String PlaceId;
	
	public double getSEQ_RecommendMenus() {
		return SEQ_RecommendMenus;
	}
	public void setSEQ_RecommendMenus(double sEQ_RecommendMenus) {
		SEQ_RecommendMenus = sEQ_RecommendMenus;
	}
	public double getSEQ_User() {
		return SEQ_User;
	}
	public void setSEQ_User(double sEQ_User) {
		SEQ_User = sEQ_User;
	}
	public double getSEQ_Post() {
		return SEQ_Post;
	}
	public void setSEQ_Post(double sEQ_Post) {
		SEQ_Post = sEQ_Post;
	}
	public String getMenu() {
		return Menu;
	}
	public void setMenu(String menu) {
		Menu = menu;
	}
	public String getPlaceId() {
		return PlaceId;
	}
	public void setPlaceId(String placeId) {
		PlaceId = placeId;
	}
}
