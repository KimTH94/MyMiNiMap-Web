package Tags;

public class TagDTO {

	private double SEQ_Tag;
	private double SEQ_User;
	private double SEQ_Post;
	private String Tag;
	private String PlaceId;
	
	public double getSEQ_Tag() {
		return SEQ_Tag;
	}
	public void setSEQ_Tag(double sEQ_Tag) {
		SEQ_Tag = sEQ_Tag;
	}
	public double getSEQ_User() {
		return SEQ_User;
	}
	public void setSEQ_User(double sEQ_User) {
		SEQ_User = sEQ_User;
	}
	public double getSEQ_Post() {
		return SEQ_Post;
	}
	public void setSEQ_Post(double sEQ_Post) {
		SEQ_Post = sEQ_Post;
	}
	public String getTag() {
		return Tag;
	}
	public void setTag(String tag) {
		Tag = tag;
	}
	public String getPlaceId() {
		return PlaceId;
	}
	public void setPlaceId(String placeId) {
		PlaceId = placeId;
	}
	
}
